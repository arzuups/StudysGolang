// SOLID PRINCIPLE
func Sum(num1 int, num2 int) int {
	return num1 + num2
}

func Extraction(num1 float32, num2 float32) float32 {
	return num1 - num2
}

func Multiplication(num1 int, num2 int) int {
	return num1 * num2
}

func Partition(num1 float64, num2 float64) float64 {
	return num1 / num2
}
